/*
Usando o método print e o método pritln 
*/
public class Prog2 {
    public static void main(String[] args) {
        System.out.println("Estamos no ano 2023");
        //impriminmdo uma palavra por linha
        System.out.println("Estamos");
        System.out.println("no");
        System.out.println("ano");
        System.out.println("2023");

        //outra forma de imprimir uma palavra por linha
        // \n é uma marcaçao de quebra de linha
        System.out.print("Estamos\nno\nano\n2023\n");

        //Tendo o mesmo efeito que o print usando o pritln
        System.out.println("Estamos\n no\n ano\n 2023");

        //impriminmdo uma palavra por linha
        System.out.print("Estamos");
        System.out.print("no");
        System.out.print("ano");
        System.out.print("2023");

        /*
        LEMBRETE
        Ao usar o println imprime um texto por linha
        Ao usar o print todos os comandos print imprimirá na mesma linha
        */
    }
}
