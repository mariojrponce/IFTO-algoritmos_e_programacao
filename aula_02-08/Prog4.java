//Código exemplo de divisão de número no java
public class Prog4 {
    public static void main(String[] args) {
        System.out.println("A divisão de 7/3 = "+7.0/3);
        System.out.print("A divisão de 7/3 = "+7.0/3.0+"\n");
        System.out.printf("A divisão de 7/3 = %.1f\n",7.0/3);
        //OBS: %f - fracionário
        //%.1f - significa que desejo apresentar apenas uma casa após a virgula
    }
}
