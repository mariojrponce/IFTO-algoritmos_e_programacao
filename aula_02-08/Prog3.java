/*
Faça uma aplicação que apresente o resultado da soma
de dois números inteiros
- usando print
- usando println
- usando printf 
*/
public class Prog3 {
    public static void main(String[] args) {
        System.out.println("A soma de 2 + 2 = "+(2+2));
        System.out.print("A soma de 2 + 2 = "+(2+2)+"\n");
        System.out.printf("A soma de 2 + 2 = %d\n",2+2);
        //obs: %d signfica que vou imprimir um numero inteiro
    }
}
