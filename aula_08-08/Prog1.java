/*
Exemplo de criação de variável local do tipo primitivo 
*/
public class Prog1{
    public static void main(String[] args) {
        byte x = 2;
        float y = 3.2F;
        char z = 'c';

        System.out.println("O valor de x = "+x);
        System.out.println("O valor de y = "+y);
        System.out.println("O valor de z = "+z);

    }
}